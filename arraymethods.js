const fruits = ["Banana", "Orange", "Apple", "Mango"];
let size = fruits.length;
console.log(`the length of array is ${size}`);

//print the array to string
console.log(`The string form of given array is ${fruits.toString()}`);

//array at()
console.log(`The string form of given array is ${fruits.at(2)}`);

//array to join method
console.log(`The string form of given array is ${fruits.join("*")}`);

//array to pop method
console.log(`The string form of given array is ${fruits.pop(2)}`);


